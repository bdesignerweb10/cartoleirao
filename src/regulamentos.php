		<p>5.3. Caso alguma pontuação seja corrigida depois da verificação da súmula ela valerá após a reabertura do Mercado, e será considerada.</p>
		<p>5.4. Critérios de desempate: Caso haja empate entre os participantes ao final do campeonato será considerado o vencedor aquele que possuir o maior número de cartoletas, se ainda assim permanecer empatados o ganhador será aquele que obtiver a maior pontuação na última rodada.</p>
